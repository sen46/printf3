int	is_option(char c)
{
	if (c == '+')
		return (1);
	else if (c == '-')
		return (2);
	else if (c == '0')
		return (3);
	else if (c == '#')
		return (4);
	else if (c == ' ')
		return (5);
	return (0);
}
